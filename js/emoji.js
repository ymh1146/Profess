function getRandomEmoji(category = null) {
  if (category && EMOJIS[category]) {
    const emojis = EMOJIS[category];
    return emojis[Math.floor(Math.random() * emojis.length)];
  } else {
    const allEmojis = Object.values(EMOJIS).flat();
    return allEmojis[Math.floor(Math.random() * allEmojis.length)];
  }
}

function replaceEmojiPlaceholders(text) {
  if (!text) return "";

  return text
    .replace(/\[emoji:([a-z]+)\]/g, (_, category) => {
      return getRandomEmoji(category);
    })
    .replace(/\[emoji\]/g, () => {
      return getRandomEmoji();
    });
}

window.EMOJIS = EMOJIS;
window.getRandomEmoji = getRandomEmoji;
window.replaceEmojiPlaceholders = replaceEmojiPlaceholders;
